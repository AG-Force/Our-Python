# Assignment Operators

x = 10  # Assigning a value of 10 to the variable 'x'

y = 20  # Assigning a value of 20 to the variable 'y'

z = x + y  # Performing addition of 'x' and 'y' and assigning the result to 'z'
print(z)  # Output: 30

print("-" * 50)  # Printing a line of dashes for visual separation

x += y  # Equivalent to x = x + y; adding 'y' to 'x' and assigning the result to 'x'
print(x)  # Output: 30

print("-" * 50)  # Printing a line of dashes for visual separation

x -= y  # Equivalent to x = x - y; subtracting 'y' from 'x' and assigning the result to 'x'
print(x)  # Output: 10

print("-" * 50)  # Printing a line of dashes for visual separation

x = x - y  # Performing subtraction of 'y' from 'x' and assigning the result to 'x'
print(x)  # Output: -10

x -= y  # Subtracting 'y' from 'x' and assigning the result to 'x'
print(x)  # Output: -30
