#---------------------------------------------------------------#
#type() um den Datentypen zu ermitteln
#All data in python are objects
#---------------------------------------------------------------#
print(type(10)) # int
print(type(10.3)) # float
print(type(-10.3)) # float
print(type(0)) # int
print(type("Hallo")) # str
print(type([1,2,3,4,5,6,7,8,9,])) #list
print(type((1,2,3,4,5,6))) #tuple
print(type({1:2,"Hallo":3})) #dictionary
print(2==2)# True
print(2!=2)# False







